#include <stdio.h>

int main(){
    printf("%d\n",10); //정수형 상수 (integer constant)
    printf("%d %d\n", 10,20);
    printf("%lf\n",3.14); //실수형 상수 (float constant)
    printf("%c\n",'a'); //문자형 상수 (character constant)
    printf("%s\n","sky"); //문자열 상수 (string constant)
    printf("10\n");
    return 0;
}
