package polymorphism;

public class LGtv {
	public void turnON() {
		System.out.println("LGtv--전원 ON");
	}
	public void turnOFF() {
		System.out.println("LGtv--전원 OFF");
	}
	public void chUP() {
		System.out.println("LGtv--다음 채널");
	}
	public void chDown() {
		System.out.println("LGtv--이전 채널");
	}
}